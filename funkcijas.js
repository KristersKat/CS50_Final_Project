function createcafeMarker(map){
    var cafeIcon = L.icon({
        iconUrl: 'cafe.png',
        iconSize: [40, 50],
        iconAnchor: [20, 40],
        popupAnchor: [-3, -76]
    });

    var cafeMarker = L.marker([56.64898330950422, 25.12054209745869], { icon: cafeIcon }).addTo(map);

    cafeMarker.on('click', function() {
        window.location.href = 'http://www.majassaldejums.lv';

    });
}

